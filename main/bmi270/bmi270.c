/* Habilita y deshabilita sensores auxiliar, giroscopio, acelermetro y temperatura. 
 * Cada parmetro recibe solo 1's o 0's. */
static void toggle_sensors(int aux, int gyr, int acc, int temp) {
    uint8_t reg_pwr_ctrl = 0x7D;
    uint8_t val_pwr_ctrl;

    if ((aux != 0 && aux != 1) || (gyr != 0 && gyr != 1) || (acc != 0 && acc != 1) || (temp != 0 && temp != 1)) {
        printf("PARMETRO INCORRECTO EN FUNCIN toggle_sensors.\n");
        exit(EXIT_SUCCESS);
    }

    val_pwr_ctrl = (temp << 3) | (acc << 2) | (gyr << 1) | aux;

    device_write(device_bmi270, &reg_pwr_ctrl, &val_pwr_ctrl, 1, TAG);
}

/* Configura el acelermetro. Variables son ODR, AVG y RANGE. Revisar datasheet
 * para ver las configuraciones disponibles. */
